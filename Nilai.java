public class Nilai {
	public static void main(String[] args){
		int nilaiA = 30;
		int nilaiB = 40;
		int nilaiC = 51;
		int nilaiD = 50;
		int nilaiE = 60;
		
		int totalNilai = nilaiA + nilaiB + nilaiC + nilaiD + nilaiE;
		double mean = totalNilai/5;
		
		System.out.println("Jumlah Total Nilai Anda adalah " + totalNilai);
		System.out.println("Rata-Rata Nilai Anda adalah " + mean);
		
	}	
}